/*Core operations

    Enqueue: Inserts a new element at the rear of the queue.
    Dequeue: Removes and returns the element from the front of the queue. 

Additional operations

    isEmpty(): Returns true if the queue is empty, false otherwise.
    isFull(): Returns true if the queue is full (for a fixed-size queue), false otherwise.
    Peek() / Front(): Retrieves the element at the front of the queue without removing it.
    Create(): Initializes an empty queue.
    Size(): Returns the number of elements in the queue. 
    */
